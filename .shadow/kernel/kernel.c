#define SIDE 16

#define IMG_WIDTH 512
#define IMG_HEIGHT 512
static int w, h;  // Screen size

#define RGB2COLOR(R, G, B) (((R) << 16) | ((G) << 8) | (B))
#define KEYNAME(key) \
  [AM_KEY_##key] = #key,
static const char *key_names[] = { AM_KEYS(KEYNAME) };

static inline void puts(const char *s) {
  for (; *s; s++) putch(*s);
}

static inline void num2str(uint32_t num, char *str) {
  char temp[128];
  int index = 0;
  for (size_t model = num % 10; num > 0; num = num / 10, model = num % 10) temp[index++] = model;
  size_t len = index;
  for (index--; index >= 0; index--) str[len - index - 1] = temp[index] + '0';
  str[len] = '\0';
}
void print_key() {
  AM_INPUT_KEYBRD_T event = { .keycode = AM_KEY_NONE };
  ioe_read(AM_INPUT_KEYBRD, &event);
  if (event.keycode != AM_KEY_NONE && event.keydown) {
    puts("Key pressed: ");
    puts(key_names[event.keycode]);
    puts("\n");
    if (event.keycode == 1) halt(0);
  }
}


static void draw_tile(int x, int y, int w, int h, uint32_t color) {
  uint32_t pixels[w * h]; // WARNING: large stack-allocated memory
  AM_GPU_FBDRAW_T event = {
    .x = x, .y = y, .w = w, .h = h, .sync = 1,
    .pixels = pixels,
  };
  for (int i = 0; i < w * h; i++) {
    pixels[i] = color;
  }
  ioe_write(AM_GPU_FBDRAW, &event);
}

static uint32_t get_pixel(int x, int y) {
  int position = (y * IMG_WIDTH + x) * 3;
  uint32_t R, G, B;
  R = (uint32_t)capybara_ppm[position];
  G = (uint32_t)capybara_ppm[position + 1];
  B = (uint32_t)capybara_ppm[position + 2];
  return RGB2COLOR(R, G, B);
}

